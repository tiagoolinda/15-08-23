let opcao = parseInt(prompt("Digite um valor de 1 a 5"));

switch (opcao) {
    case 1:
        alert("Você digitou o numero 1");
        break;
    case 2:
        alert("Você digitou o numero 2");
        break;
    case 3:
        alert("Você digitou o numero 3");
        break;
    case 4:
        alert("Você digitou o numero 4");
        break;
    case 5:
        alert("Você digitou o numero 5");
        break;


    default:
        alert("Você digitou um numero invalido");
}